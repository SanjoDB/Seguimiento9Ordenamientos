import java.util.Scanner;

public class Solution5 {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        int t = sc.nextInt();

        for (int i = 0; i < t; i++) {

            int n = sc.nextInt();

            int[] w = new int[n];
            int[] s = new int[n];
            for (int j = 0; j < n; i++) {
                w[j] = sc.nextInt();
            }

            for (int j = 0; j < n; i++) {
                s[j] = sc.nextInt();
            }
        }
    }
}
